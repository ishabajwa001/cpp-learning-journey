#include <iostream>
#include "Operations.h"
using namespace std;

// Add 2 numbers
float performAddition(int num1,float num2){
    return num1+num2;
}

// Subtract 2 numbers
float performSubstraction(int num1,float num2){
    return num1-num2;
}

// Multiply 2 numbers
float performMultiplication(int num1,float num2){
    return num1*num2;
}

// Perform Division
float performDivision(int num1,float num2){
    return num1/num2;
}

