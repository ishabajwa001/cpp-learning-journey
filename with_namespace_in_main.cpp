#include <iostream>

int main(){
    using namespace std;
    cout<<"Hello "<<"World"<<endl;
}

/*
    This program prints "Hello World" on the console.

    - #include <iostream> gives access to input and output features.
    - int main() is the starting point of the program.
    - using namespace std; allows us to use cout and endl without std::.
    - cout is used to display output on the screen.
    - << sends text to the output stream.
    - endl moves the cursor to the next line.
    - The output of this program is:

    Hello World
*/

